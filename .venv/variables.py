# variables can be named with lower or upper case letters, numbers

cars = 21
CARS = 22

# variables can start with an underscore

_cars = 23

# variables are case-sensitive

bags = 24
Bags = 25

# multi name variables should have an underscore between each word

kind_of_cars = "Ferrari"
kind_of_bags = "Telfar"

# when two variable names are the same, python will call the last variable
SHOES = "LV"
SHOES = "Hermes"


# print the result if the variable

print(cars)
print(CARS)
print(_cars)
print(bags)
print(Bags)
print(kind_of_cars)
print(kind_of_bags)
print(SHOES)
